function simulateLoading1() {
    var loadingBar = document.getElementById('loading-bar1');
    
 
    loadingBar.style.width = '100%'; 
    loadingBar.style.backgroundColor = '#00ff00'; 
    
 
    setTimeout(function() {
        loadingBar.style.width = '50%'; 
        loadingBar.style.backgroundColor = '#ffff00'; 
        
       
        setTimeout(function() {
            loadingBar.style.width = '10%'; 
            loadingBar.style.backgroundColor = '#ff0000'; 
            
         
            setTimeout(function() {
                window.location.href = '../JuegoVisual/patronNivelUno.html';
            }, 2000); 
        }, 2000); 
    }, 2000);
}




